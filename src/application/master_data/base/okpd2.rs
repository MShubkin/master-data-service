fn is_okpd2_code_prefix(s: &str) -> bool {
    let mut digit_count = 0;
    let mut segment_count = 0;

    let segment_ok = |digit_count, segment_count, allow_empty| {
        // первый сегмент всегда состоит из двух цифр
        if segment_count == 0 {
            digit_count == 2
        } else {
            segment_count < 4
                && (allow_empty || digit_count > 0)
                && digit_count <= 3
        }
    };

    for ch in s.chars() {
        match ch {
            '0'..='9' => {
                digit_count += 1;
            }
            '.' if segment_ok(digit_count, segment_count, false) => {
                digit_count = 0;
                segment_count += 1;
            }
            _ => {
                return false;
            }
        }
    }
    // разрешаем последний пустой сегмент
    segment_ok(digit_count, segment_count, true)
}

