#[derive(Debug, Default)]
pub struct Okpd2Directory(pub(crate) MasterDataCommonDirectory<Okpd2>);

#[async_trait]
impl MasterDataDirectoryInterface<Okpd2> for Okpd2Directory {
    async fn load(&mut self, pool: &PgPool) -> MasterDataResult<()> {
        self.0.load(pool).await
    }

    async fn get_by_ids(
        &self,
        ids: &[i32],
    ) -> MasterDataResult<(Messages, Vec<Okpd2>)> {
        self.0.get_by_ids(ids).await
    }

    async fn get_by_id(&self, id: &i32) -> MasterDataResult<Okpd2> {
        self.0.get_by_id(id).await
    }

    async fn search(
        &self,
        search_request: &SearchByUserInput,
    ) -> MasterDataResult<(Messages, Vec<Okpd2>)> {
        let SearchByUserInput {
            from,
            search,
            quantity,
        } = search_request;
        let map = self.0.data.read().await;
        let records = map.values();
        let result = if is_okpd2_code_prefix(search) {
            records
                .filter(|record| record.code.starts_with(search))
                .skip(*from as usize)
                .take(*quantity as usize)
                .cloned()
                .collect()
        } else {
            let search = search_request.search.trim().to_lowercase();
            records
                .filter(|record| contains_case_insensitive(&record.text, &search))
                .skip(*from as usize)
                .take(*quantity as usize)
                .cloned()
                .collect()
        };
        Ok((Messages::default(), result))
    }

    async fn get_updates(
        &self,
        timestamp: AsezTimestamp,
    ) -> MasterDataResult<Vec<Okpd2>> {
        self.0.get_updates(timestamp).await
    }

    async fn get_full_data(&self) -> MasterDataResult<Vec<Okpd2>> {
        self.0.get_full_data().await
    }
}

#[async_trait]
impl HierarchyGenerator for Okpd2Directory {
    async fn generate(&self) -> MasterDataResult<(&'static str, Hierarchy)> {
        let items = self.get_full_data().await?;
        Ok(("okpd2", okpd2_generate_hierarchy(items).into()))
    }
}

/// Геренация иерархии идентификаторов кодов ОКПД2 на основе строковых кодов.
///
/// ```ignore
/// (2,'01','Продукция и услуги сельского хозяйства и охоты'),
/// (3,'01.1','Культуры однолетние'),
/// (4,'01.11','Культуры зерновые (кроме риса), зернобобовые, семена масличных культур'),
/// (5,'01.11.1','Пшеница'),
/// (6,'01.11.11','Пшеница твердая'),
/// (7,'01.11.11.110','Пшеница озимая твердая'),
/// (8,'01.11.11.111','Зерно озимой твердой пшеницы'),
/// (9,'01.11.11.112','Семена озимой твердой пшеницы'),
/// (10,'01.11.11.120','Пшеница яровая твердая'),
/// (11,'01.11.11.121','Зерно яровой твердой пшеницы'),
/// (12,'01.11.11.122','Семена яровой твердой пшеницы'),
/// (13,'01.11.11.130','Зерноотходы твердой пшеницы'),
/// (14,'01.11.12','Пшеница, кроме твердой пшеницы'),
/// ...
/// ```
fn okpd2_generate_hierarchy(mut items: Vec<Okpd2>) -> AHashMap<i32, HierarchyData> {
    // 1) сортируем по коду, таким образом все дети находятся после родителей
    items.sort_by(|a, b| a.code.cmp(&b.code));

    let mut stack: Vec<(String, i32)> = Vec::new(); // (code, id)
    let mut hierarchy = AHashMap::with_capacity(items.len());

    for Okpd2 {
        id,
        code,
        is_removed,
        from_date,
        to_date,
        ..
    } in items
    {
        // 2) Откатываемся, пока верхушка стека не является родителем.
        while let Some((parent_code, _)) = stack.last() {
            if is_parent(parent_code, &code) {
                break;
            }
            stack.pop();
        }

        // 3) Собираем id родителей (корень … текущий уровень-1).
        let parents: Vec<i32> = stack.iter().map(|&(_, pid)| pid).collect();
        hierarchy.insert(
            id,
            HierarchyData {
                parents,
                code: code.clone(),
                is_removed,
                from_date,
                to_date,
            },
        );

        // 4) Текущий узел может стать родителем следующих.
        stack.push((code, id));
    }

    hierarchy
}

/// Возвращает `true`, если `parent` является родителем `child`.
///
/// Применяется в алгоритме [построения иерархии кодов ОКПД2](okpd2_generate_hierarchy),
/// работает только в случае `parent` < `child`.
fn is_parent(parent: &str, child: &str) -> bool {
    debug_assert!(
        parent < child,
        "предикат работает только на кодах, сортированных по возрастанию"
    );

    // Случай 1: ребёнок длиннее и начинается со строки-родителя.
    // parent: 01.02.03
    // child:  01.02.03.40
    if child.len() > parent.len() && child.starts_with(parent) {
        return true;
    }
    // Случай 2: одинаковая длина, но у родителя в конце больше 0.
    // Бывает только для категорий, т.е. заполнены все позиции.
    //
    // parent: 01.02.03.100
    // child:  01.02.03.140
    //
    // parent: 01.02.03.140
    // child:  01.02.03.141
    const MAX_LEN: usize = "XX.XX.XX.XXX".len();
    if parent.len() == child.len()
        && parent.len() == MAX_LEN
        && parent.ends_with('0')
    {
        let child = child.trim_end_matches('0');
        let parent = parent.trim_end_matches('0');
        return child.len() > parent.len();
    }
    false
}

